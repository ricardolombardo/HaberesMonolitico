IF NOT EXISTS (SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_NAME = 'CONCEPTO')
BEGIN
    CREATE TABLE CONCEPTO (
        ID INT IDENTITY(1,1) PRIMARY KEY,
        NOMBRE NVARCHAR(255) NOT NULL,
        SALDO NVARCHAR(255),
        CODIGO NVARCHAR(100),
        REMUNERATIVO BIT NOT NULL DEFAULT 0
    );
    CREATE INDEX idx_CODIGO ON CONCEPTO (CODIGO);
END
GO