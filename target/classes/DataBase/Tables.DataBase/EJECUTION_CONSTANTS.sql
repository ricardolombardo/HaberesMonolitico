IF NOT EXISTS (
    SELECT *
    FROM INFORMATION_SCHEMA.TABLES
    WHERE TABLE_NAME = 'EJECUTION_CONSTANTS'
)
BEGIN
    CREATE TABLE EJECUTION_CONSTANTS (
        ID INT IDENTITY(1,1) PRIMARY KEY,
        [KEY] NVARCHAR(100) NOT NULL,
        VALUE_ NVARCHAR(255) NOT NULL,
        [DESCRIPTION] NVARCHAR(500)
    );

    CREATE UNIQUE INDEX IDX_EJECUTION_CONSTANTS_KEY
        ON EJECUTION_CONSTANTS ([KEY]);
END
GO
